export function translate(
  language: LanguageCode,
  key: TranslationKey,
  replacements?: Record<string, string | number>,
) {
  let value = translations[language][key] ?? translations.en[key];

  if (
    key === "availableUnitCount" &&
    replacements &&
    typeof replacements.count === "number"
  ) {
    return language === "zh-Hant"
      ? replacements.count === 1
        ? "1 個可售單位"
        : `${replacements.count} 個可售單位`
      : replacements.count === 1
        ? "1 unit"
        : `${replacements.count} units`;
  }

  if (replacements) {
    Object.entries(replacements).forEach(([token, replacement]) => {
      value = value.replace(`{${token}}`, String(replacement)) as typeof value;
    });
  }

  return value;
}

export function translateDisplay(language: LanguageCode, value: string) {
  return displayTranslations[language][value] ?? value;
}
